package sesion02_mul_a;

public class polar {
private float radio;
private float angulo;


public polar(float radio, float angulo) {
	super();
	this.radio = radio;
	this.angulo = angulo;
}

public float getAngulo() {
	return angulo;
}

public void setAngulo(float angulo) {
	this.angulo = angulo;
}

public float getRadio() {
	return radio;
}

public void setRadio(float radio) {
	this.radio = radio;
}

}
